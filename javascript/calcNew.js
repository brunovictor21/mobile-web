// Funções 
function somar (n1,n2) {
    console.log("somar");
console.log (n1+n2)

}

// sub 
function sub (n1,n2) {
    console.log("subtrair");
console.log (n1-n2)
}
//mult
function mult (n1,n2) {
    console.log("multiplicar");
console.log (n1*n2)
}
//div
function div (n1,n2) {
    console.log("Dividir");
console.log (n1/n2)
}
// Imprimir Funções 
somar(10,10); // 10+10
somar(10,20); // 10+20
somar(10,30); // 10+30
somar(3, 3); // 3+3
somar(15, 10); // 15+10

sub(10,10); // 10-10
sub(10,20); // 10-20
sub(10,30); // 10-30
sub(3, 3); // 3+3
sub(15, 10); // 15+10


mult(10,10); // 10*10
mult(10,20); // 10*20
mult(10,30); // 10*30
mult(3, 3); // 3+3
mult(15, 10); // 15+10

div(10,10); // 10/10
div(10,20); // 10/20
div(10,30); // 10/30
div(3, 3); // 3/3
div(15, 10); // 15/10